                  <h2>Intro About CSIA.</h2>
                  <p>Chhatrapati Shivaji Maharaj International Airport, Mumbai (IATA: BOM, ICAO: VABB), formerly known as Sahar International Airport, is the primary international airport serving the Mumbai Metropolitan Area, India.
					  It is the second busiest airport in the country in terms of total and international passenger traffic after Delhi, and was the 14th busiest airport in Asia and 28th busiest airport in the world by passenger traffic in calendar year 2017.
					  Its passenger traffic was about 49.8 million in year 2018. The airport is the second busiest in the country in terms of cargo traffic also.
					  In March 2017, the airport overtook London Gatwick Airport as the world''s busiest airport with only one operational runway at a time.</p>
				  <p>The airport is operated by Mumbai International Airport Limited (MIAL), a Joint Venture between the Airports Authority of India and the GVK Industries Ltd led consortium which was appointed in February 2006 to carry out the modernisation of the Airport.
					   The new integrated terminal T2 was inaugurated on 10 January 2014[13] and opened for international operations on 12 February 2014.
					   A dedicated six lane, elevated road connecting the new terminal with the main arterial Western Express Highway was also opened to the public the same day.</p>
				  <p>The airport is named after the 17th-century Maratha king Chhatrapati Shivaji, having been renamed in 1999 from the previous "Sahar Airport" to "Chhatrapati Shivaji International Airport" (the title "Maharaj" was inserted on 30 August 2018).
					   	CSIA's IATA airport code – "BOM" – is derived from Bombay, Mumbai's former name.
					   It is situated across the suburbs of Santacruz and Sahar village in Vile Parle East.</p>
